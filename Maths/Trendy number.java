// A three digit no. whose middle no. is divisible by three is called trendy no.

public class trendyno {
    public static void main(String[] args) {
        int st= 100;
        int en = 999;
        for(int i = st; i<=en;i++){
            int n =i;
            int x = (n/10)%10;
            if(x%3==0 && x!=0){
                System.out.print(n+" ");
            }
        }
    }
    
}

